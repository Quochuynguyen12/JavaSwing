    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        if(vitri == -1){
            JOptionPane.showMessageDialog(this,"bạn cần chọn nhân viên");
        }else{
            liststudent.remove(vitri);
            vitri--;
            loadtable();
            JOptionPane.showMessageDialog(this,"xoá thành công");

        }
    }//GEN-LAST:event_btnxoaActionPerformed
int vitri =-1;
    public void filltocontro(int vitri){
        txtmasv.setText(liststudent.get(vitri).getMaSV());
        txttensv.setText(liststudent.get(vitri).getTenSV());
        
        if(liststudent.get(vitri).getGioitinh()==true){
            bnam.setSelected(true);
        }else{
            bnu.setSelected(true);
        }
        Cnamsinh.setSelectedIndex(liststudent.get(vitri).getNamsinh());
    }
    private void tblListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListMouseClicked
        // TODO add your handling code here:
        vitri=tblList.getSelectedRow();
        filltocontro(vitri);
    }//GEN-LAST:event_tblListMouseClicked

