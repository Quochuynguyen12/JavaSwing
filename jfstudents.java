    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        Student nv = new Student();
     nv.setMaSV(txtmasv.getText());
     nv.setTenSV(txttensv.getText());
     boolean gt=true;
     if(bnam.isSelected()){
         gt=true;
     }else{
         gt=false;
     }
     nv.setGioitinh(gt);
     nv.setNamsinh(Cnamsinh.getSelectedIndex());
     
     liststudent.add(nv);
     
     // do luu lieu len table
     loadtable();
    }//GEN-LAST:event_btnsaveActionPerformed
//ham đổ lưu liệu lên table
    public void loadtable(){
       DefaultTableModel model = (DefaultTableModel)tblList.getModel();
       // đổ từ dòng đầu tiên
       model.setRowCount(0);
       String gt;
       String namsinh;
       for(Student nv : liststudent){
           if(nv.getGioitinh() == true){
               gt="Nam";
           }else{
               gt = "nữ";
           }
//Boolean gioiTinh = liststudent.get(vitri).getGioitinh();
//if (gioiTinh != null) {
//    int selectedIndex = gioiTinh ? 1 : 0;
//    Cnamsinh.setSelectedIndex(selectedIndex);
//} else {
//    // Xử lý trường hợp giá trị gioiTinh là null
//    // Có thể đặt một giá trị mặc định hoặc xử lý theo ý muốn của bạn
//}
           if(nv.getNamsinh() == 0)
           {
               namsinh="2004";
           }else if(nv.getNamsinh() == 1){
               namsinh="2003";
           }else if(nv.getNamsinh() == 2){
               namsinh="2002";
           }else{
               namsinh="2001";
           }
           model.addRow(new Object[] {nv.getMaSV(),nv.getTenSV(),gt,namsinh});
       }
       
   }
